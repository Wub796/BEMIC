export class TranslationSystem {
  constructor(defaultLang = 'en') {
    this.currentLanguage = localStorage.getItem('bemic_lang') || defaultLang;
    this.listeners = [];
  }

  init() {
    this.applyTranslations();
  }

  setLanguage(lang) {
    if (this.currentLanguage === lang) return;
    this.currentLanguage = lang;
    localStorage.setItem('bemic_lang', lang);
    
    // Smooth transition: fade out, translate, fade in
    document.body.classList.add('translating');
    
    setTimeout(() => {
      this.applyTranslations();
      this.listeners.forEach(cb => cb(lang));
      
      setTimeout(() => {
        document.body.classList.remove('translating');
      }, 150);
    }, 150);
  }

  applyTranslations() {
    const dict = translations[this.currentLanguage];
    
    // Find all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      if (dict[key]) {
        // Handle input placeholders differently
        if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
          el.placeholder = dict[key];
        } else {
          el.innerHTML = dict[key];
        }
      }
    });

    // Update document title and html lang attribute
    document.documentElement.lang = this.currentLanguage;
    document.title = this.currentLanguage === 'zh' 
      ? "双语早期数学创新中心 - BEMIC" 
      : "Bilingual Early Mathematics Innovation Center - BEMIC";
  }

  onChange(callback) {
    this.listeners.push(callback);
  }
}
